//* Promises ->
//? Promises is an object that represent the eventual completion or failure of an asynchronous operations. 


//* Callback -> 
//? A JavaScript callback is a function which is to be executed after another function has finished execution.
//? Any function that is passed as an argument to another function so that it can be executed in that other function is called as callback function.


//* Callback Hell -> 
//? Callbck hell is the situation in javaScript where multiple nested callbacks create complex, deeply indented code, often called as "Pyramid of Doom".  This structure make code difficult to read, debug, and maintain, resulting in poor code quality and scalability issues. 


//* Async and Await -> 
//? Async and Await in JavaScript is used to simplify handling asynchronous operations using promises. By enabling asynchronous code to appear synchronous, they enhance code readability and make it easier to manage complex asynchronous flows.



//* Async Function
//? The async function allows us to write promise-based code as if it were synchronous. This ensures that the execution thread is not blocked. Async functions always return a promise. If a value is returned that is not a promise, JavaScript automatically wraps it in a resolved promise.


//* Await keyword -> 
//? The await keyword is used to wait for a promise to resolve. It can only be used within an async block. Await makes the code wait until the promise returns a result, allowing for cleaner and more manageable asynchronous code.


//* Closure -> 
//? A function along with its parent lexical environment. 
//? A closure in JavaScript is a function that has access to its outer scope, or the lexical environment, even after the outer function has finished executing.
  

